import numpy as np

a = np.array([3, 4.5, 2.1, 6.4, 9.8, 2, 5, 10.5, 11.2, 8.7])

print("a: ", a)
print("min: ", a.min())
print("max: ", a.max())
print("argmin: ", a.argmin())
print("argmax: ", a.argmax())
print("mean: ", a.mean())
print("sum: ", a.sum())
print("cumsum: ", a.cumsum())
print("prod: ", a.prod())
print("cumprod: ", a.cumprod())
